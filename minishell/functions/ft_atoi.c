#include "./../includes/minishell.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while ((str[i] < '0') && (str[i] > '9'))
		i += 1;
	while (str[i] != '\0' && (str[i] >= '0') && (str[i] <= '9'))
	{
		n *= 10;
		n += (int)str[i] - '0';
		i += 1;
	}
	return (n);
}
